#include <stdlib.h>

int	ft_atoi(char *str)
{
	int y;
	int w;

	y = 0;
	w = 1;
	while (*str && (*str == ' ' || *str == '\n' || *str == '\t' ||
			*str == '\v' || *str == '\f' || *str == '\r'))
		str++;
	if (*str == '-')
		w = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str != '\0')
	{
		if (*str >= '0' && *str <= '9')
		{
			y = 10 * y + (*str - '0');
		}
		else
			return (y * w);
		str++;
	}
	return (y * w);
}

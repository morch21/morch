#include "libft.h"

void	ft_print_arr(char **s)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		ft_putchar('[');
		ft_putnbr(i);
		write(1, "]=", 2);
		ft_putstr(s[i++]);
		write(1, ";\n", 2);
	}
}
